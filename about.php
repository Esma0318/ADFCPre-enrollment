            <p><b>Asian Development Foundation College (ADFC)</b> is a private educational institution in Tacloban City. It was founded in 1984 as a pre-elementary school, which eventually developed into a higher institution of learning. When it opened its high school department, ADFC integrated complete computer subjects in the curriculum, making it the first business high school in the region. Through a holistic education promoting academic excellence, research, and community service, ADFC commits to developing its students into competent and responsible professionals. <br><br>

<b>ADFC</b> currently provides programs from the preschool level to the graduate level. The institution has the Academic and the Technical-Vocational-Livelihood (TVL) tracks for Senior High School (SHS) students. For college students, there is a variety of bachelor's, associate, and diploma courses in Engineering, Criminology, Teacher Education, Social Science, Mass Communication, Hotel and Tourism Mangement, Business Administration, Allied Health, and Information Technology. ADFC also offers a Bachelors of Law program. The Graduate School department, on the other hand, has programs in Teacher Education, Business Administration, Humanities, and Information Technology.
<br>
<br>
Aside from these said programs, the college also offers various technical-vocational courses classified under: Tourism/ Hotel and Restaurant; Construction; Electronics; Garments; Information and Communication Technology; Health, Social, and Community Development Services; Metal and Engineering; and, Processed Food and Beverages. These are for students who are interested in training for particular skills and earning their National Certificate (NC).
<br>
<br>
<b>Asian Development Foundation College</b> and its programs are duly recognized by the Commission on Higher Education (CHED), the Department of Education (DepEd), and the Technical Education and Skills Development Authority (TESDA). The institution is also a member of the Philippine Association of Colleges and Universities (PACU). 

